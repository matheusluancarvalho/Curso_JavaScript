/*

                            Comparadores de Comparação

Maior que ... >
Maior que ou igual a ... >=
Menor que ... <
Menor que ou igual a ... <=
Igualdade ... == (valor)
Igualdade estrita ... === (valor e tipo)
Diferente ... != (valor)
Diferente estrito ... !== (valor e tipo)

*/

console.log(10 > 5);   // Dez é maior que cinco
console.log(10 >= 5); // Dez é maior ou igual a cinco

console.log(10 == 10);
console.log(10 === 10);  // Checa além do valor, checa o tipo

console.log(10 !== 10); // false